type DetailRegionOption = {
  code: DetailRegionCode
  label: string
  region: RegionCode
}

function createDetailRegionOptions(
  region: RegionCode,
  options: Array<readonly [DetailRegionCode, string]>,
): DetailRegionOption[] {
  return options.map(([code, label]) => ({
    code,
    label,
    region,
  }))
}

